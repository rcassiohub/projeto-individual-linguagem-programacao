    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        String InsiraRestoDoProduto = tfSolucao.getText();
        Double qtdRestoDeProduto = Double.valueOf(InsiraRestoDoProduto); 
       
      //Quantos economizará em relação a perda. 
      // Exemplo: se ele o cliente tem 2000 produtos e perdeu 200
      // nossa solução reduzará 90% desse 200
      //logo, ficará 180, ou seja, estamos evitando perda de 20 produtos
       Double resultado = qtdRestoDeProduto * 0.90;
        
       lblValorSolucao.setText(resultado.toString());
      
    }//GEN-LAST:event_btnConfirmarActionPerformed

