    private void btnVerficarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerficarProdutoActionPerformed
       String InsiraProduto = tfCampoProdutosTotal.getText();
       Double QtdProduto = Double.valueOf(InsiraProduto);
       
      String InsiraProdutoPerdido = tfCampoProdutosPerdidos.getText();
      Integer QtdProdutoPerdido = Integer.valueOf(InsiraProdutoPerdido);
      
      String puxandoProdutoPerdido = tfCampoProdutosPerdidos.getText();
      tfSolucao.setText(puxandoProdutoPerdido);
      
      Double calcularProduto = QtdProduto - QtdProdutoPerdido; 
        
      Double economia = calcularProduto / QtdProduto;
       
      Double porcentagem = 100 - (economia * 100);
       
      lblQtdProdutoPerdido.setText(calcularProduto.toString());
       
      lblResultado.setText(String.format("%.1f%%", porcentagem));
      
    }//GEN-LAST:event_btnVerficarProdutoActionPerformed

